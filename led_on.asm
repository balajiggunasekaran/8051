;BLINK AN LED AT 1HZ FREQUENCY.
;CLOCK SOURCE = 11.0592 MHZ, 12 OSCILLATIONS = 1 MACHINE CYCLE; HENCE CPU SPEED = 11.0592/12 = 921.6 KHZ. NEED TO TOGGLE EVERY 921.6 K MACHINE CYCLES
; IT TAKES 460800 MACHINE CYCLES FOR 500MS
; NEED A 5 DIGIT HEX NUMBER TO HOLD THIS VALUE. OR 3 BYTE OF DATA

;NUMBERS DETERMINES THE BLINKING FREQUENCY
LOOP:   MOV R0, #000H; 1 CYCLE LOWER COUNT BYTE
	MOV R1, #0E0H; 1 CYCLE UPPER COUNT BYTE
	CPL P2.0; TOGGLE LED, 1 MACHINE CYCLE
DECR0:  NOP; 1 CYCLE
	NOP; 1 CYCLE
	NOP; 1 CYCLE
	NOP; 1 CYCLE
	NOP; 1 CYCLE
	NOP; 1 CYCLE
	DJNZ R0, DECR0; 2 CYCLES 
; TOTAL 8 CYCLES PER LSB OF LOWER BYTE (0XE000 * 8)
DECR1:  DEC R1; 1 CYCLE
	MOV A, R1; 1 CYCLE
	JZ LOOP; 2 CYCLES
	SJMP DECR0; 2 CYCLES
; TOTAL 6 CYCLES PER LSB OF UPPER BYTE (0XE0 * 6)
	END
; 8X + 6Y = 460800
; X -256Y = 0